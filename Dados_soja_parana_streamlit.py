import pandas as pd
import streamlit as st
import plotly.express as px

st.set_page_config(layout="wide", page_title="GeoAgro - Dashboard Interativo", page_icon="ğŸŒ¾")

df = pd.read_csv("dados.csv")

colunas_novas = {
    'Safra': 'safra',
    'Regiao': 'regiao',
    'Municipio': 'municipio',
    'Latitude': 'latitude',
    'Longitude': 'longitude',
    'Altitude': 'altitude',
    'Rendimento (Kg/ha)': 'rendimento_kg_ha',
    'ReduÃ§ao-chuvas (%)': 'reducao_chuvas_perc',
    'Queda Rendimento 18-19': 'queda_rendimento_perc'
}
df.rename(columns=colunas_novas, inplace=True)

def classificar_regiao(latitude):
    if latitude >= -24:
        return 'Norte_Parana'
    elif latitude < -24 and latitude >= -25.5:
        return 'Centro_Parana'
    else:
        return 'Sul_Parana'

df['regiao_geo'] = df['latitude'].apply(classificar_regiao)

st.title("ğŸŒ¾ GeoAgro Dashboard - AnÃ¡lise de Risco ClimÃ¡tico da Soja no ParanÃ¡")

st.sidebar.header("ğŸ¯ Filtros de VisualizaÃ§Ã£o")
regioes = sorted(df['regiao_geo'].unique())
regiao_selecionada = st.sidebar.selectbox("Selecione uma RegiÃ£o:", regioes)

df_filtrado = df[df['regiao_geo'] == regiao_selecionada]

st.markdown(f"### RegiÃ£o Selecionada: **{regiao_selecionada}**")

fig = px.scatter_mapbox(
    df_filtrado,
    lat='latitude',
    lon='longitude',
    color='queda_rendimento_perc',
    size='rendimento_kg_ha',
    hover_name='municipio',
    hover_data={
        'rendimento_kg_ha': ':.2f} kg/ha',
        'queda_rendimento_perc': ':.2f}%',
        'reducao_chuvas_perc': ':.2f}%'
    },
    color_continuous_scale=px.colors.sequential.Reds,
    zoom=6,
    height=600
)
fig.update_layout(mapbox_style='open-street-map', margin={'r':0,'t':0,'l':0,'b':0})
st.plotly_chart(fig, use_container_width=True)

st.markdown("#### ğŸ“Š Ranking dos MunicÃ­pios - Queda de Rendimento (%)")
tabela_resumo = df_filtrado[['municipio', 'rendimento_kg_ha', 'queda_rendimento_perc']].sort_values('queda_rendimento_perc', ascending=False)
st.dataframe(tabela_resumo, use_container_width=True)

st.markdown("#### ğŸ” CorrelaÃ§Ã£o entre ReduÃ§Ã£o de Chuvas e Queda de Rendimento")
correlacao = df_filtrado[['reducao_chuvas_perc', 'queda_rendimento_perc']].corr().iloc[0,1]
st.metric("CorrelaÃ§Ã£o (Ï)", f"{correlacao:.2f}")

